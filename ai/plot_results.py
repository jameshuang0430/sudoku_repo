from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Sequence

import matplotlib.pyplot as plt


TRAINING_KEYS = [
    ("train_loss", "Train Loss"),
    ("blank_cell_accuracy", "Blank Cell Accuracy"),
    ("board_solved_rate", "Board Solved Rate"),
    ("valid_board_rate", "Valid Board Rate"),
]
EVALUATION_RATE_KEYS = [
    ("blank_cell_accuracy", "Blank Cell Accuracy"),
    ("board_solved_rate", "Board Solved Rate"),
    ("valid_board_rate", "Valid Board Rate"),
]
EVALUATION_CONFLICT_KEYS = [
    ("mean_mismatch_count", "Mean Mismatch Count"),
    ("mean_row_conflicts", "Mean Row Conflicts"),
    ("mean_col_conflicts", "Mean Col Conflicts"),
    ("mean_box_conflicts", "Mean Box Conflicts"),
    ("mean_total_conflicts", "Mean Total Conflicts"),
    ("mean_postprocess_change_count", "Mean Postprocess Change Count"),
    ("mean_decode_iteration_count", "Mean Decode Iteration Count"),
]


def parse_args(argv: Sequence[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Render training or evaluation metrics to a PNG chart.")
    parser.add_argument("--input", type=Path, required=True, help="JSON report generated by ai.train or ai.eval.")
    parser.add_argument("--output", type=Path, help="PNG output path. Defaults to the input path with a .png suffix.")
    parser.add_argument("--title", help="Optional chart title override.")
    return parser.parse_args(argv)


def main(argv: Sequence[str] | None = None) -> None:
    args = parse_args(argv)
    with args.input.open("r", encoding="utf-8") as handle:
        payload = json.load(handle)

    output_path = resolve_output_path(args.input, args.output)

    if isinstance(payload.get("epochs"), list):
        plot_training_report(payload, output_path, args.title)
    elif isinstance(payload.get("metrics"), dict):
        plot_evaluation_report(payload, output_path, args.title)
    else:
        raise ValueError("Unsupported report format. Expected a training metrics report or an evaluation report.")

    print(f"saved_plot={output_path}")


def resolve_output_path(input_path: Path, output_path: Path | None) -> Path:
    if output_path is not None:
        return output_path
    return input_path.with_suffix(".png")


def plot_training_report(payload: dict[str, object], output_path: Path, title: str | None) -> None:
    epochs = payload.get("epochs", [])
    if not epochs:
        raise ValueError("Training report must contain at least one epoch record.")

    epoch_numbers = [record["epoch"] for record in epochs]
    figure, axes = plt.subplots(2, 2, figsize=(12, 8))
    for axis, (key, label) in zip(axes.flat, TRAINING_KEYS):
        values = [record[key] for record in epochs]
        axis.plot(epoch_numbers, values, marker="o", linewidth=2)
        axis.set_title(label)
        axis.set_xlabel("Epoch")
        axis.set_ylabel(label)
        axis.grid(True, alpha=0.3)

    figure.suptitle(title or "Training Metrics")
    figure.tight_layout()
    save_figure(figure, output_path)


def plot_evaluation_report(payload: dict[str, object], output_path: Path, title: str | None) -> None:
    metrics = payload.get("metrics", {})
    rate_values = [metrics[key] for key, _ in EVALUATION_RATE_KEYS if key in metrics]
    rate_labels = [label for key, label in EVALUATION_RATE_KEYS if key in metrics]
    conflict_values = [metrics[key] for key, _ in EVALUATION_CONFLICT_KEYS if key in metrics]
    conflict_labels = [label for key, label in EVALUATION_CONFLICT_KEYS if key in metrics]

    if conflict_values:
        figure, axes = plt.subplots(1, 2, figsize=(14, 5))
        rate_axis, conflict_axis = axes
    else:
        figure, rate_axis = plt.subplots(figsize=(8, 5))
        conflict_axis = None

    rate_bars = rate_axis.bar(rate_labels, rate_values, color=["#2d6a4f", "#1d3557", "#bc4749"][: len(rate_values)])
    rate_axis.set_ylim(0.0, 1.0)
    rate_axis.set_ylabel("Score")
    rate_axis.set_title("Accuracy / Validity")
    rate_axis.grid(True, axis="y", alpha=0.3)

    for bar, value in zip(rate_bars, rate_values):
        rate_axis.text(bar.get_x() + bar.get_width() / 2, value + 0.02, f"{value:.3f}", ha="center")

    if conflict_axis is not None:
        conflict_bars = conflict_axis.bar(conflict_labels, conflict_values, color="#d97706")
        conflict_axis.set_ylabel("Count")
        conflict_axis.set_title("Mismatch / Conflict Summary")
        conflict_axis.grid(True, axis="y", alpha=0.3)
        conflict_axis.tick_params(axis="x", rotation=20)
        upper = max(conflict_values) if conflict_values else 1.0
        conflict_axis.set_ylim(0.0, max(1.0, upper * 1.2))
        for bar, value in zip(conflict_bars, conflict_values):
            conflict_axis.text(bar.get_x() + bar.get_width() / 2, value + max(0.05, upper * 0.03), f"{value:.2f}", ha="center")

    figure.suptitle(title or "Evaluation Metrics")
    figure.tight_layout()
    save_figure(figure, output_path)


def save_figure(figure: plt.Figure, output_path: Path) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    figure.savefig(output_path, dpi=160, bbox_inches="tight")
    plt.close(figure)


if __name__ == "__main__":
    main()
